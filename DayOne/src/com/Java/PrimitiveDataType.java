package com.Java;

public class PrimitiveDataType {

	public static void main(String[] args) {
		// declaration 
		
		int a;
		
		a = 30;
		
		System.out.println(a);
		
		byte b = 45;
		System.out.println(b);
		
		char n = 'c';
		System.out.println(n);
		
		float f = 10.25f;
		System.out.println(f);

		double d = 10.25;
		System.out.println(d);
		
		boolean bool = true;
		System.out.println(bool);
		
		
		
		
		
		

	}

}
